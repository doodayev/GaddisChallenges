#include <iostream>
using namespace std;

int main()
{
    cout << "The size of a character is " << sizeof(char) << endl;
    cout << "The size of an integer is " << sizeof(int) << endl;
    cout << "The size of a float is " << sizeof(float) << endl;
    cout << "The size of a double is " << sizeof(double) << endl;
    return 0;
}
    
